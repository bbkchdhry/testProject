package array;

public class One {

	private int x[];
	private boolean y[];
	private double z[];

	public int[] getX() {
		return x;
	}

	public void setX(int[] x) {
		this.x = x;
	}

	public boolean[] getY() {
		return y;
	}

	public void setY(boolean[] y) {
		this.y = y;
	}

	public double[] getZ() {
		return z;
	}

	public void setZ(double[] z) {
		this.z = z;
	}


}
